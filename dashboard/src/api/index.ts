// ── Error type guard ──

interface ApiRequestError {
  code: string;
  message?: string;
}

export function isApiRequestError(e: unknown): e is ApiRequestError {
  return (
    typeof e === "object" &&
    e !== null &&
    "code" in e &&
    typeof (e as ApiRequestError).code === "string"
  );
}
