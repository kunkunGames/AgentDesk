#!/usr/bin/env python3
"""Create a portable AgentDesk operator starter scaffold."""

from __future__ import annotations

import argparse
import json
import os
import shlex
from dataclasses import dataclass
from pathlib import Path
from typing import Any


PRIVATE_MARKERS = (
    "/Users/" "itismyfield",
    "mac-mini-release",
    "DISCORD_BOT_TOKEN",
    "MEMENTO_ACCESS_KEY=",
)


@dataclass(frozen=True)
class FileSpec:
    path: Path
    content: str
    label: str


@dataclass(frozen=True)
class DirSpec:
    path: Path
    label: str


def env_path(name: str) -> Path | None:
    value = os.environ.get(name, "").strip()
    return Path(value).expanduser() if value else None


def default_root() -> Path:
    return env_path("AGENTDESK_ROOT_DIR") or Path.home() / ".adk" / "release"


def as_resolved_path(value: Path) -> Path:
    return value.expanduser().resolve()


def path_string(path: Path) -> str:
    return path.as_posix()


def yaml_string(value: str) -> str:
    return json.dumps(value)


def shell_export(name: str, value: Path | str) -> str:
    return f"export {name}={shlex.quote(str(value))}"


def commented_shell_export(name: str, value: Path | str) -> str:
    return f"# {shell_export(name, value)}"


def starter_config(root: Path) -> str:
    root_s = path_string(root)
    shared_prompt = path_string(root / "config" / "agents" / "_shared.prompt.md")
    agent_prompt = path_string(root / "config" / "agents" / "ch-td.prompt.md")
    workspace = path_string(root / "workspaces" / "ch-td")
